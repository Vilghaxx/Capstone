/**
 * Get tooth region by tooth number
 * @param {number} toothNumber - 1-32
 * @returns {object} tooth region object
 */
export const getToothRegion = (toothNumber) => {
  return TOOTH_REGIONS[toothNumber] || null;
};

/**
 * Get all teeth in a specific quadrant
 * @param {string} quadrant - 'UR' | 'UL' | 'LL' | 'LR'
 * @returns {array} array of tooth regions
 */
export const getToothsByQuadrant = (quadrant) => {
  return Object.values(TOOTH_REGIONS).filter(
    tooth => tooth.quadrant === quadrant
  );
};

/**
 * Get tooth type classification
 * @param {number} toothNumber - 1-32
 * @returns {string} tooth type
 */
export const getToothType = (toothNumber) => {
  const tooth = TOOTH_REGIONS[toothNumber];
  return tooth ? tooth.type : null;
};

/**
 * Validate tooth number
 * @param {number} toothNumber
 * @returns {boolean}
 */
export const isValidToothNumber = (toothNumber) => {
  return toothNumber >= 1 && toothNumber <= 32 && Number.isInteger(toothNumber);
};

/**
 * Get all tooth numbers
 * @returns {array} [1, 2, 3, ..., 32]
 */
export const getAllToothNumbers = () => {
  return Object.keys(TOOTH_REGIONS).map(Number).sort((a, b) => a - b);
};
